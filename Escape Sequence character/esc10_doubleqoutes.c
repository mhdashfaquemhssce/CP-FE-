/* 10. Double Quote Escape Sequence In C (\")
The double quote escape sequence is used
when we want to insert a double quotation mark in the output.

Just like in the case of single quotes,
the double quotation character also has a predefined meaning in C code,
i.e., to assign string values/ enclose string literals.
The double quote escape sequence hence inserts the double quote character wherever it is placed. */

#include <stdio.h>

int main()
{
    printf("I am \"Unstoppable\" ");

    return 0;
}


